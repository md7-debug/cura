#!/usr/bin/env node
import { mkdirSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { readings } from "../src/content/readings.js";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const readingDirectory = path.join(root, "public", "readings");
const catalogFile = path.join(root, "src", "content", "readingCatalog.generated.js");

function wordCount(paragraphs) {
  return paragraphs.join(" ").trim().split(/\s+/u).filter(Boolean).length;
}

function catalogLocale(content) {
  const words = wordCount(content.text);
  return {
    language: content.language,
    minutes: Math.max(2, Math.ceil(words / 220)),
    preview: content.preview,
    title: content.title,
  };
}

mkdirSync(readingDirectory, { recursive: true });

for (const reading of readings) {
  writeFileSync(
    path.join(readingDirectory, `${reading.number}.json`),
    `${JSON.stringify(reading)}\n`,
  );
}

const catalog = readings.map((reading) => ({
  author: reading.author,
  authorId: reading.authorId,
  code: reading.code,
  en: catalogLocale(reading.en),
  fr: catalogLocale(reading.fr),
  number: reading.number,
  sources: reading.sources,
  work: reading.work,
}));

writeFileSync(
  catalogFile,
  `// Generated by scripts/prepare-reading-data.mjs.\nexport const readingCatalog = ${JSON.stringify(catalog, null, 2)};\n`,
);

console.log(`Prepared ${readings.length} on-demand readings and the lightweight catalog.`);
